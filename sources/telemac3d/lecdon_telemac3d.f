!  SPATIAL PROJECTION TYPE CHECKED IF SPHERICAL COORDINATES ASKED
!
      IF(SPHERI) THEN
        IF(PROTYP.NE.2.AND.PROTYP.NE.3) THEN
          IF(LNG.EQ.1) THEN
            WRITE(LU,*) 'TYPE DE PROJECTION SPATIALE : ',PROTYP
            WRITE(LU,*) 'IMPOSSIBLE AVEC DES COORDONNEES SPHERIQUES'
          ENDIF
          IF(LNG.EQ.2) THEN
            WRITE(LU,*) 'SPATIAL PROJECTION TYPE: ',PROTYP
            WRITE(LU,*) 'IMPOSSIBLE WITH SPHERICAL COORDINATES'
          ENDIF
          CALL PLANTE(1)
          STOP
        ENDIF
      ENDIF
!
!-----------------------------------------------------------------------
!
!  CHECKING COHERENCE FOR 2D COMPUTATION CONTINUED
!
      IF(SUIT2) THEN
        IF(T3D_FILES(T3DS2D)%NAME(1:1).EQ.' ') THEN
          IF(LNG.EQ.1) THEN
            WRITE(LU,*) 'SUITE 2D :'
            WRITE(LU,*) 'DONNER UN : FICHIER POUR SUITE 2D'
          ENDIF
          IF(LNG.EQ.2) THEN
            WRITE(LU,*) '2D CONTINUATION:'
            WRITE(LU,*) 'GIVE A: FILE FOR 2D CONTINUATION'
          ENDIF
          CALL PLANTE(1)
          STOP
        ENDIF
!       BOTH TYPES OF CONTINUATIONS ARE FORBIDDEN
        IF(.NOT.DEBU) THEN
          IF(LNG.EQ.1) THEN
            WRITE(LU,*) 'SUITE 2D & SUITE DE CALCUL'
            WRITE(LU,*) 'NE PEUVENT ETRE SIMULTANEMENT A OUI'
          ENDIF
          IF(LNG.EQ.2) THEN
            WRITE(LU,*) '2D CONTINUATION & CONTINUATION CONTINUED:'
            WRITE(LU,*) 'CANNOT BE BOTH SET TO TRUE'
          ENDIF
          CALL PLANTE(1)
          STOP
        ENDIF
      ENDIF
!
!-----------------------------------------------------------------------
!
!   REACTS IF A PREVIOUS COMPUTATION FILE IS REQUIRED, BUT NOT PROVIDED
!
      IF(.NOT.DEBU.AND.T3D_FILES(T3DPRE)%NAME(1:1).EQ.' ') THEN
        IF(LISTIN) THEN
          IF(LNG.EQ.1) WRITE(LU,151)
          IF(LNG.EQ.2) WRITE(LU,152)
        ENDIF
151     FORMAT(1X,'LECDON : UNE SUITE DE CALCUL EST DEMANDEE',/,10X,
     &  'IL FAUT DONNER UN FICHIER DU CALCUL PRECEDENT',/)
152     FORMAT(1X,'LECDON : A CONTINUED COMPUTATION REQUIRED, ',/,
     &         1X,' A PREVIOUS COMPUTATION FILE IS NECESSARY',/)
        CALL PLANTE(1)
        STOP
      ENDIF
!
!-----------------------------------------------------------------------
! This may not be useful anymore ..
!      IF(SEDI.AND.SETDEP.EQ.1) THEN
!        IF((SCHCTA(1).NE.ADV_LPO_TF).AND.(SCHCTA(1).NE.ADV_NSC_TF)) THEN
! error message
!          IF(LNG.EQ.1) WRITE(LU,153) ADV_NSC_TF
!          IF(LNG.EQ.2) WRITE(LU,154) ADV_NSC_TF
! change value
!          SCHCTA(1)=ADV_NSC_TF
!        ENDIF
!      ENDIF
!
153      FORMAT(1X,'LECDON : FOR EXPLICIT SETTLING SCHEME',/,10X,
     &  'USE CONVECTION SCHEME ',I2/)
154      FORMAT(1X,'LECDON : POUR SCHEMA EXPLICITE',/,10X,
     &  'UTILISER CONVECTION SCHEME ',I2/)
!
!-----------------------------------------------------------------------
!
!     ATMOSPHERE-WATER EXCHANGE MODEL AND WIND COMPATIBILITY
      IF(INCLUS(COUPLING,'WAQTEL').AND..NOT.VENT) THEN
        IF(WAQPROCESS.NE.1.AND.WAQPROCESS.NE.4)THEN
!
        IF(LNG.EQ.1) WRITE(LU,181)
        IF(LNG.EQ.2) WRITE(LU,182)
 181    FORMAT(///,1X,'ATTENTION ! MODULE D''ECHANGES EAU-',/,1X,
     &                  'ATMOSPHERE ACTIVE MAIS PAS LE VENT.',/,1X,
     &                  'LES COMPOSANTES HORIZONTALES DE VENT',/,1X,
     &                  'NE SONT PAS PRISES EN COMPTE',/,1X,
     &                  'HORS DE CE MODULE',///)
 182    FORMAT(///,1X,'BEWARE! ATMOSPHERE-WATER EXCHANGE',/,1X,
     &                  'MODEL ACTIVATED, BUT NOT THE WIND.',/,1X,
     &                  'THE HORIZONTAL COMPONENTS OF WIND',/,1X,
     &                  'ARE NOT TAKEN INTO ACCOUNT',/,1X,
     &                  'OUTSIDE THIS MODULE',///)
        ENDIF
      ENDIF
!
!-----------------------------------------------------------------------
!
!     ATMOSPHERE-WATER EXCHANGE MODEL AND ATMOSPHERIC PRESSURE COMPATIBILITY
      IF(INCLUS(COUPLING,'WAQTEL').AND..NOT.ATMOS) THEN
        IF(WAQPROCESS.NE.1.AND.WAQPROCESS.NE.4)THEN
!
        IF(LNG.EQ.1) WRITE(LU,183)
        IF(LNG.EQ.2) WRITE(LU,184)
 183    FORMAT(///,1X,'ATTENTION ! MODULE D''ECHANGES EAU-',/,1X,
     &                  'ATMOSPHERE ACTIVE MAIS PAS LA PRESSION,',/,1X,
     &                  'QUI N''EST PAS PRISE EN COMPTE HORS DE',/,1X,
     &                  'CE MODULE',///)
 184    FORMAT(///,1X,'BEWARE! ATMOSPHERE-WATER EXCHANGE',/,1X,
     &                  'MODEL ACTIVATED, BUT NOT THE PRESSURE,',/,1X,
     &                  'WHICH IS NOT TAKEN INTO ACCOUNT',/,1X,
     &                  'OUTSIDE THIS MODULE',///)
        ENDIF
      ENDIF
!
!-----------------------------------------------------------------------
!
!     CONSISTENCY OF KEYWORDS ON WAVE DRIVEN CURRENTS
!
      IF(COUROU.AND..NOT.INCLUS(COUPLING,'TOMAWAC').AND.
     &   T3D_FILES(T3DBI1)%NAME.EQ.' ') THEN
        IF(LNG.EQ.1) WRITE(LU,185)
        IF(LNG.EQ.2) WRITE(LU,186)
185     FORMAT(1X,'LECDON : LES COURANTS DE HOULE SONT DEMANDES',/,10X,
     &  'IL FAUT LES DONNER DANS UN FICHIER',/,10X,
     &  'OU FAIRE DU COUPLAGE AVEC TOMAWAC' )
186     FORMAT(1X,'LECDON: WAVE DRIVEN CURRENTS ARE REQUESTED, ',/,8X,
     &  ' EITHER IN A FILE OR BY COUPLING WITH TOMAWAC',/)
        CALL PLANTE(1)
        STOP
      ENDIF
!
!-----------------------------------------------------------------------
!
!     ATMOSPHERE-WATER EXCHANGE MODEL AND TEMPERATURE COMPATIBILITY
      IF(INCLUS(COUPLING,'WAQTEL').AND.IND_T.EQ.0) THEN
        IF(WAQPROCESS.NE.1.AND.WAQPROCESS.NE.4)THEN
!
        IF(LNG.EQ.1) WRITE(LU,187)
        IF(LNG.EQ.2) WRITE(LU,188)
 187    FORMAT(///,1X,'ATTENTION ! MODULE D''ECHANGES EAU-',/,1X,
     &                  'ATMOSPHERE ACTIVE MAIS LE TRACEUR',/,1X,
     &                  'TEMPERATURE N EXISTE PAS !',///)
