      ENDIF
!
!-----------------------------------------------------------------------
! SPECIAL TREATMENT IF PARALLELISM
!
      IF(NCSIZE.GT.1.AND.BANDEC.AND.OPTBAN.EQ.2) THEN
        OPTBAN=1
